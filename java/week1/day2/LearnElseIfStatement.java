package week1.day2;

public class LearnElseIfStatement {

	public static void main(String[] args) 
	{
		
		// when we have more than 2 case then we should use elseif
		int num=0;
		if(num<0) 
		{
			System.out.println("Print the num");
		}
		
	
		else if(num>0)
		{
			System.out.println("the given number is not satisfied");
		}
		else 
		{
			System.out.println("the number is 0");
		}

	}

}
